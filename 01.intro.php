    <h1>SQL Intro</h1>

    <p>SQL is a standard language for accessing and manipulating databases.</p>

    <p>SQL stands for Structured Query Language</p>

    <p>SQL lets you access and manipulate databases.</p>

    <p>SQL is an ANSI (American National Standards Institute) standard.</p>

    <hr />

    <h2>What Can SQL Do?</h2>

    <ul>
      <li>execute queries against a database</li>
      <li>retrieve data from a database</li>
      <li>insert records in a database</li>
      <li>update records in a database</li>
      <li>delete records from a database</li>
      <li>create new databases</li>
      <li>create new tables in a database</li>
      <li>create stored procedures in a database</li>
      <li>create views in a database</li>
      <li>set permissions on tables, procedures and views</li>
    </ul>

    <hr />

    <h2>SQL is a Standard - But...</h2>

    <p>There are different versions of the SQL language.</p>

    <p>However, in order to be compliant with the ANSI standard, they all support at least the major commands (such as SELECT, UPDATE, DELETE, INSERT, WHERE) in a similar manner.</p>

    <div class="w3-panel w3-border-red w3-leftbar w3-pale-blue">
      <p><strong>Note:</strong> Most of the sql database programs also have their own proprietary extensions in addition to the SQL standard.</p>
    </div>

    <hr />

    <h2>Using SQL In Your Website</h2>

    <p>To build a website that shows data from a database, you will need:</p>

    <ul>
      <li>An RDBMS database program (like MySQL)</li>
      <li>A server side scripting language (like PHP)</li>
      <li>To use SQL to get the data that you want</li>
      <li>To use HTML/CSS to style the page</li>
    </ul>

    <hr />

    <h2>RDBMS</h2>

    <p>RDBMS stands for Relational Database Management System</p>

    <p>RDBMS is the basis for SQL, and for all modern database systems such as MS SQL Server, IB DB2, Oracle, MySQL, and Microsoft Access.</p>

    <p>The data in RDBMS is stored in database objects called tables. A table is a collection of related data entries and it consists of columns and rows.</p>

    <p>Every table is broken up into smaller entities called fields. A field is a column in a table that is designed to maintain specific information about every record in the table.</p>

    <p>A record, also called a row, is each individual entry that exists in a table. A record is a horizontal entity in a table.</p>

    <p>A column is a vertical entity in a table that contains all information associated with a specific field in a table.</p>

