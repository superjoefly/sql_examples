    <h1>Create Database Statement</h1>

    <p>The CREATE DATABASE statement is used to create a new database.</p>

    <h3>Syntax:</h3>

    <pre><code class="language-sql">
      CREATE DATABASE databasename;
    </code></pre>

    <hr />

    <h3>Example: CREATE DATABASE</h3>

    <pre><code class="language-sql">
      CREATE DATABASE testDB;
    </code></pre>

    <hr />

    <h2>SHOW DATABASES</h2>

    <p>To show a list of databases, use the SHOW DATABASES statement:</p>

    <pre><code class="language-sql">
      SHOW DATABASES;
    </code></pre>

