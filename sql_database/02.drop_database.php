    <p>The DROP DATABASE Statement is used to drop an existing SQL database.</p>

    <h3>Syntax:</h3>

    <pre><code class="language-sql">
      DROP DATABASE databasename;
    </code></pre>

    <hr />

    <h3>Example:</h3>

    <pre><code class="language-sql">
      DROP DATABASE testDB;
    </code></pre>

