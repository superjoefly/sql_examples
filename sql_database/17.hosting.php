    <h1>Hoisting</h1>

    <p>If you want your web site to be able to store and retrieve data from a database, your web server should have access to a database-system that uses the SQL language.</p>

    <p>If your web server is hosted by an Internet Service Provider (ISP), you will have to look for SQL hosting plans.</p>

    <p>The most common SQL hosting databases are MS SQL Server, Oracle, MySQL, and MS Access.</p>

    <hr />

    <h2>MySQL</h2>

    <p>MySQL is a popular database software for web sites.</p>

    <p>MySQL is a very powerful, robust and full featured SQL database system.</p>

    <p>MySQL is an inexpensive alternative to other SQL solutions.</p>

